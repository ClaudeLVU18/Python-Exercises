# Exercise 1: Basic Math ☑️
# Using math module compute the following

import math

# For a =2.3 find the ceil of a
a = 2.3
ceil_a = math.ceil(a)
print(ceil_a)  # Output: 3

# For a =2.3 find the floor of a
floor_a = math.floor(a)
print(floor_a)  # Output: 2

# For a = 5 find the factorial of a
factorial_a = math.factorial(5)
print(factorial_a)  # Output: 120

# Find the value of 2 to the power of3
value = 2 ** 3
print(value)  # Output: 8

# For a=16 find the square root of a
sqrt_a = math.sqrt(16)
print(sqrt_a)  # Output: 4.0
